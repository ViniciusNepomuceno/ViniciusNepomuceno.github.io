// Comprimento de uma string
console.log("Vinicius Nepomuceno".length);

// Conversão de caso
console.log("Vinicius Nepomuceno".toUpperCase());
console.log("Vinicius Nepomuceno".toLowerCase());

// Busca em strings
console.log("Vinicius Nepomuceno".indexOf("Nepomuceno"));
console.log("Vinicius Nepomuceno".includes("Vinicius"));

// Extração de substrings
console.log("Vinicius Nepomuceno".substring(0, 8));
console.log("Vinicius Nepomuceno".slice(9));

// Substituição
console.log("Vinicius Nepomuceno".replace("Vinicius", "Rasgese"));

// Divisão em array
console.log("Vasco da Gama".split(","));

// Remoção de espaços
console.log("Vinicius Nepomuceno".trim());